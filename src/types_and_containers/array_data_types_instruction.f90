    DOUBLE PRECISION,   INTENT(IN   ):: VAL
    TYPE (ID_VAL_TYPE):: EXT
    !
    TYP%INUSE = TRUE
    !
    IF(TYP%N == Z) THEN
                  TYP%N = ONE
                  !
                  ALLOCATE( TYP%PT(TYP%N) )
                  TYP%PT(TYP%N)%ID = ID;   TYP%PT(TYP%N)%VAL = VAL
    ELSE
                  EXT%N = TYP%N + ONE
                  !
                  ALLOCATE( EXT%PT (EXT%N) )
                  EXT%PT(:TYP%N)  = TYP%PT
                  !
                  EXT%PT(EXT%N)%ID = ID;   EXT%PT(EXT%N)%VAL = VAL
                  !
                  CALL MOVE_ALLOC(EXT%PT,  TYP%PT)
                  TYP%N = EXT%N
    END IF
