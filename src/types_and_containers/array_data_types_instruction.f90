  PURE FUNCTION JOIN_TXT_CHAR(TXT,JOIN,ENDING) RESULT(LN)
    CHARACTER(*),DIMENSION(:),CONTIGUOUS,INTENT(IN):: TXT
    CHARACTER(*),                        INTENT(IN):: JOIN
    CHARACTER(*), OPTIONAL,              INTENT(IN):: ENDING
    CHARACTER(:),                       ALLOCATABLE:: LN
