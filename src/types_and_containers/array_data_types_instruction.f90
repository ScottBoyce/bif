  PURE ELEMENTAL SUBROUTINE GROW_SIZE_CHAR_BUF_TYPE(CH, N) !Check if you can add N letters
    CLASS(CHARACTER_BUF_TYPE), INTENT(INOUT):: CH
    INTEGER,                   INTENT(IN   ):: N
    INTEGER:: DIM, SIZ
    !
    SIZ = CH%N+N
    DIM = CH%DIM
    IF(SIZ > DIM) THEN
                       IF(DIM < 64) DIM = 64
                       !
                       IF (DIM < 8192) THEN
                           DO WHILE (SIZ > DIM)
                                           DIM = 2*DIM
                           END DO
                       ELSE
                           DO WHILE (SIZ > DIM)
                                           DIM = DIM + 2048
                           END DO
                       END IF
                       CALL SET_SIZE_CHAR_BUF_TYPE(CH, DIM)
    END IF
    !
  END SUBROUTINE
