  PURE FUNCTION NEW_DIM_SIZE(DIM) RESULT(DIM_NEW)
     INTEGER, INTENT(IN):: DIM
     INTEGER:: DIM_NEW
     !
     IF    (DIM <  64) THEN
                        DIM_NEW = 64
     ELSEIF(DIM < 512) THEN
                        DIM_NEW = TWO * DIM
     ELSEIF(DIM < 4096) THEN
                        DIM_NEW =  512*(DIM/512)  + 512
     ELSE
                        DIM_NEW = 1024*(DIM/1024) + 1024
     END IF
     !
  END FUNCTION
