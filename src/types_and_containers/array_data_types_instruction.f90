     INTEGER,              INTENT(IN   ):: POS,VAL
     !
     IF(IT%N < ONE) CALL ALLOCATE_INTEGER_VECTOR(IT,POS)
     !
     IF(POS  <=  IT%N) THEN
          IT%VEC(POS) = VAL
     ELSEIF(POS > IT%N) THEN
        BLOCK 
            TYPE(INTEGER_VECTOR):: IT2
            CALL IT%MOVE(IT2)
            CALL ALLOCATE_INTEGER_VECTOR(IT,POS)
            CALL IT2%COPY(IT)
            IT%VEC(POS) = VAL
        END BLOCK
     END IF
  END SUBROUTINE
