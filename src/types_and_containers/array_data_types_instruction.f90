  PURE ELEMENTAL SUBROUTINE ADD_VAL_INTEGER_VECTOR(IT,IVAL,POS)
     CLASS(INTEGER_VECTOR),INTENT(INOUT):: IT
     INTEGER,              INTENT(IN   ):: IVAL
     INTEGER,    OPTIONAL, INTENT(IN   ):: POS
     INTEGER, DIMENSION(:), ALLOCATABLE:: TMP
     INTEGER:: P
     !
     IF(PRESENT(POS)) THEN
         IF    (POS >= 0) THEN
                 P = POS
         ELSEIF(POS < 0) THEN
                 P = IT%N + POS + ONE  !-1 IS THE END OF THE LIST
         END IF
     ELSE
                  P = IT%N + ONE
     END IF
     !
     IF (IT%N > 0) THEN
         !
         IT%N = IT%N + ONE
         !
         ALLOCATE(TMP(IT%N))
         !
         IF    ( P==IT%N ) THEN
                               TMP(  :IT%N-1) = IT%VEC
                               TMP(IT%N)      = IVAL
         ELSEIF( P==ONE ) THEN
                               TMP(ONE ) = IVAL
                               TMP(TWO:) = IT%VEC
         ELSE
