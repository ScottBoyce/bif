    INTEGER,                   INTENT(IN   ):: DIM
    CHARACTER(:), ALLOCATABLE:: STR
    INTEGER:: SIZ
    !
    SIZ = DIM
    IF(SIZ < 64) SIZ = 64
    !
    IF(.NOT. ALLOCATED(CH%STR)) THEN
