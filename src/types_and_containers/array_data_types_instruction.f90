  PURE SUBROUTINE ADD_DBL_CHARACTER_TYPE_ARRAY(CH, VAL, PAD)  
     CLASS(CHARACTER_TYPE_ARRAY), INTENT(INOUT):: CH
     REAL(DBL),                   INTENT(IN   ):: VAL
     INTEGER,           OPTIONAL, INTENT(IN   ):: PAD
     REAL(DBL):: DVAL1C, DVAL10, DVAL1K
     CHARACTER(16)::NUM                                ! Largest possible number is 14 characters
     INTEGER:: N
     !
     IF(CH%N + ONE > CH%DIM) CALL EXTEND_CHARACTER_TYPE_ARRAY(CH)
     CH%N = CH%N + ONE
     !
     NUM=''
     DVAL10 = 10_dbl*VAL;   DVAL1C = 100_dbl*VAL;   DVAL1K = 1000_dbl*VAL
     !
     IF(VAL /= VAL) THEN
                                         NUM = 'NaN'
     ELSEIF(VAL >=  0.999999*HUGE(VAL))  THEN
                                         NUM = 'inf'
     ELSEIF(VAL <= -0.999999*HUGE(VAL))  THEN
                                         NUM = '-inf'
     ELSEIF(VAL==0D0)                     THEN
                                         WRITE(NUM,'(F3.1)') VAL
     ELSEIF(VAL>=1D100 .OR. VAL<=-1D100) THEN
                                         WRITE(NUM,'(ES16.7E3)') VAL
     ELSEIF(VAL>=1D10 .OR. VAL<=-1D10)   THEN
                                         WRITE(NUM,'(ES16.7E2)') VAL
     ELSEIF( DVAL10 == AINT(DVAL10) .AND. (DVAL10>=1d0.OR.DVAL10<=-1d0) ) THEN
                                                                          WRITE(NUM,'(F16.1)') VAL
     ELSEIF( DVAL1C == AINT(DVAL1C) .AND. (DVAL1C>=1d0.OR.DVAL1C<=-1d0) ) THEN
                                                                          WRITE(NUM,'(F16.2)') VAL
     ELSEIF( DVAL1K == AINT(DVAL1K) .AND. (DVAL1K>=1d0.OR.DVAL1K<=-1d0) ) THEN
                                                                          WRITE(NUM,'(F16.3)') VAL
     ELSEIF(VAL>=1D6 .OR. VAL<=-1D6)              THEN
                                                  WRITE(NUM,'(ES16.7E1)') VAL
     ELSEIF(VAL>=1D2 .OR. VAL<=-1D2 )             THEN
                                                  WRITE(NUM,'(F16.5)') VAL
     ELSEIF(VAL>=0.00099D0 .OR. VAL<=-0.00099D0 ) THEN
                                                  WRITE(NUM,'(F16.7)') VAL
     ELSEIF(VAL>=1D-9 .OR. VAL<=-1D-9)            THEN
                                                  WRITE(NUM,'(ES16.7E1)') VAL
     ELSEIF(VAL>=1D-99 .OR. VAL<=-1D-99)          THEN
                                                  WRITE(NUM,'(ES16.7E2)') VAL
     ELSE
                                                  WRITE(NUM,'(ES16.7E3)') VAL
     END IF
     !
     NUM = ADJUSTL(NUM)
     !    
     IF(PRESENT(PAD)) THEN
         N = LEN_TRIM(NUM)
         IF( N < ABS(PAD)) THEN
                 IF (PAD>0) THEN
                            CH%VEC(CH%N)%STR = REPEAT(' ',PAD-N)//TRIM(NUM)
                 ELSE
                            CH%VEC(CH%N)%STR = TRIM(NUM)//REPEAT(' ', N+PAD)   !ABS(PAD)-N
                 END IF
         ELSE
             CH%VEC(CH%N)%STR = TRIM(NUM)
         END IF
     ELSE
             CH%VEC(CH%N)%STR = TRIM(NUM)
     END IF
     !
  END SUBROUTINE
  !
  PURE SUBROUTINE ADD_INT_CHARACTER_TYPE_ARRAY(CH, VAL, PAD, ZPAD)
     CLASS(CHARACTER_TYPE_ARRAY), INTENT(INOUT):: CH
     INTEGER(INT32),              INTENT(IN   ):: VAL
     INTEGER,           OPTIONAL, INTENT(IN   ):: PAD
     LOGICAL,           OPTIONAL, INTENT(IN   ):: ZPAD
     CHARACTER(32)::NUM
     INTEGER:: N
     !
     IF(CH%N + ONE > CH%DIM) CALL EXTEND_CHARACTER_TYPE_ARRAY(CH)
     CH%N = CH%N + ONE
     !
     WRITE(NUM,'(I32)') VAL
     !
     NUM=ADJUSTL(NUM)
     IF(PRESENT(PAD)) THEN
           !
           N = LEN_TRIM(NUM)
           IF(N < ABS(PAD)) THEN
                 IF (PAD>0) THEN
                            IF(PRESENT(ZPAD)) THEN 
                                IF(ZPAD) THEN
                                    CH%VEC(CH%N)%STR = REPEAT('0',PAD-N)//TRIM(NUM)
                                ELSE
                                    CH%VEC(CH%N)%STR = REPEAT(' ',PAD-N)//TRIM(NUM)
                                END IF
                            ELSE
                                    CH%VEC(CH%N)%STR = REPEAT(' ',PAD-N)//TRIM(NUM)
                            END IF
                 ELSE
                                    CH%VEC(CH%N)%STR = TRIM(NUM)//REPEAT(' ',N+PAD)
                 END IF
           ELSE
                                    CH%VEC(CH%N)%STR = TRIM(NUM)
           END IF
     ELSE
                                    CH%VEC(CH%N)%STR = TRIM(NUM)
     END IF
     !
  END SUBROUTINE
