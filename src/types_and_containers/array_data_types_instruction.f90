  PURE SUBROUTINE BUILD_COMPRESSED_LOCATION_STORAGE_ALOC(LAC, ARR, HAS_ZERO, ID)
    CLASS (COMPRESSED_LOCATION_STORAGE),DIMENSION(:),ALLOCATABLE, INTENT(INOUT):: LAC
    INTEGER, DIMENSION(:,:),                          CONTIGUOUS, INTENT(IN   ):: ARR
    LOGICAL,                                            OPTIONAL, INTENT(IN   ):: HAS_ZERO
    INTEGER, DIMENSION(:),ALLOCATABLE,                  OPTIONAL, INTENT(INOUT):: ID
    INTEGER:: I,J,K,N,DIM1,DIM2,SIZ
    INTEGER, DIMENSION(:), ALLOCATABLE:: TMP1, TMP2
    LOGICAL:: DO_Z
    !
    DIM1 = SIZE(ARR, ONE)
    DIM2 = SIZE(ARR, TWO)
    !
    IF(PRESENT(HAS_ZERO)) THEN
        DO_Z = HAS_ZERO
    ELSE
        DO_Z = FALSE
    END IF
    !
    ALLOCATE(TMP1(64), SOURCE=Z)
    SIZ = 64
    N   = Z
    !
    IF(DO_Z) THEN
       DO J=ONE,DIM2
       DO I=ONE,DIM1
           IF( N==Z ) THEN
                                               N = N + ONE
                                               TMP1(ONE) = ARR(I,J)
           ELSEIF( ALL(ARR(I,J) /= TMP1(:N)) ) THEN
               !
               N = N + ONE
               IF(N > SIZ) THEN
                   ALLOCATE(TMP2(N+63))
                   TMP2(:SIZ) = TMP1
                   CALL MOVE_ALLOC(TMP2, TMP1)
                   SIZ = N+50
               END IF
               !
               TMP1(N) = ARR(I,J)
           END IF
       END DO
       END DO
    ELSE
       DO J=ONE,DIM2
       DO I=ONE,DIM1
         IF(ARR(I,J) /= Z) THEN
           IF( N==Z ) THEN
                                               N = N + ONE
                                               TMP1(ONE) = ARR(I,J)
           ELSEIF( ALL(ARR(I,J) /= TMP1(:N)) ) THEN
               !
               N = N + ONE
               IF(N > SIZ) THEN
                   ALLOCATE(TMP2(N+63))
                   TMP2(:SIZ) = TMP1
                   CALL MOVE_ALLOC(TMP2, TMP1)
                   SIZ = N+50
               END IF
               !
               TMP1(N) = ARR(I,J)
           END IF
         END IF
       END DO
       END DO
    END IF
    !
    ! SORT VALUES
    !
    DO I=2, N
        K=TMP1(I)
        J=I
        DO WHILE ( J > ONE )
                           IF (TMP1(J-ONE) < K) EXIT
                           TMP1(J) = TMP1(J-ONE)
                           J=J-ONE
        END DO
        TMP1(J) = K
    END DO
    !
    IF(ALLOCATED(LAC)) THEN
        IF(SIZE(LAC) /= N) THEN
            DEALLOCATE(LAC)
              ALLOCATE(LAC(N))
        END IF
    ELSE
        ALLOCATE(LAC(N))
    END IF
    !
    DO CONCURRENT(I=ONE:N)
        CALL BUILD_COMPRESSED_LOCATION_STORAGE_DIM2(LAC(I), TMP1(I), ARR)
    END DO
    !
    IF(PRESENT(ID)) THEN
        IF(ALLOCATED(ID)) THEN
          IF(SIZE(ID) /= N) THEN
              DEALLOCATE(ID)
                ALLOCATE(ID(N))
          END IF
        ELSE
            ALLOCATE(ID(N))
        END IF
        !
        ID = TMP1(:N)
    END IF
    !
  END SUBROUTINE
