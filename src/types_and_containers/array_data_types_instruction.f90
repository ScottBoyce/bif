  PURE SUBROUTINE ADD_UNIQUE_CHARACTER_TYPE_ARRAY(CH, STR, IGNORE_CASE)
     CLASS(CHARACTER_TYPE_ARRAY), INTENT(INOUT):: CH
     CHARACTER(*),                INTENT(IN   ):: STR
