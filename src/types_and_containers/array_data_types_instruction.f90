                                CH%STR(:) = BLNK
                                CH%N   = Z
                                CH%DIM = SIZ
    ELSEIF(SIZ > CH%DIM) THEN
                                ALLOCATE(CHARACTER(SIZ):: STR)
                                STR(:) = CH%STR
                                CALL MOVE_ALLOC(STR,CH%STR)
                                CH%DIM = SIZ
    END IF
    !
  END SUBROUTINE
